    <script type="text/javascript">
	$(document).ready(function(){
		var q_select=$(".select_box");
		for (var i=0;i<q_select.length;i++){
			q_select.eq(i).width(q_select.eq(i).find(".option").width());
		}
		$(".select_box").click(function(event){   
			event.stopPropagation();
			$(this).find(".option").toggle();
			$(this).parent().siblings().find(".option").hide();
		});
		$(document).click(function(event){
			var eo=$(event.target);
			if($(".select_box").is(":visible") && eo.attr("class")!="option" && !eo.parent(".option").length)
			$('.option').hide();									  
		});
		/*赋值给文本框*/
		$(".select_month .option a").click(function(){
			var value=$(this).text();
			$(this).parent().siblings(".select_txt").text(value);
			$("#select_value").val(value)
		})
		$(".select_bags .option a").click(function(){
			var value=$(this).text();
			$(this).parent().siblings(".select_txt").text(value);
			$("#select_value").val(value);
			if(value==1){
				$(".bags_span").text("bag");
			}else{
				$(".bags_span").text("bags");	
			}
			$(".enroll span").text((15*value).toFixed(2));
		})
		$(".select_month .option a").click(function(){
			var value=$(this).text();
			$(this).parent().siblings(".select_txt").text(value);
			$("#select_value").val(value);
			if(value==1){
				$(".month_span").text("month");
			}else{
				$(".month_span").text("months");	
			}
			
		})
		$(".enroll a").click(function(){
			$("#layer_bg").show();
		    $("#layer_div").fadeIn();	
		});
	})
	$(document).bind("click",function(e){
		var target  = $(e.target);
		if(target.closest("#layer_con").length == 0&&target.closest(".enroll a").length == 0){
		   $("#layer_bg").hide();
		   $("#layer_div").fadeOut();
		}
	}) 
</script>
